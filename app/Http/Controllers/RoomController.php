        $room->data = json_encode($request->all());
        $saved = $room->save();

        $room->room_id = $room->id;
        $tmp = json_decode($room->data);
        $tmp->id = $room->id;

        $room->data = json_encode($tmp);
        $saved = $room->save();

        return response()->json([
            'success' => 1,
            'room_id' => $room->id,
            'add' => $request->all(),
            'saved' => $saved,
        ]);
    }

